package chap03;

public class WhileExam01 {

	public static void main(String[] args) {

		int i = 1;

		while (i <= 10) {
			System.out.println(i);
			i++;
		} // end while

		int k = 1;

		do {
			System.out.println(k);
			k++;
		} while (k <= 10); // end do~while

	}// end main

}// end WhileExam01

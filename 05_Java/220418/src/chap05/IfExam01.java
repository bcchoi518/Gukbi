package chap05;

public class IfExam01 {

	public static void main(String[] args) {

		int x = 10, y = 20;

		if (x < y) {
			System.out.println("x가 y보다 작습니다.");
		} else if (x == y) {
			System.out.println("x와 y가 같습니다.");
		} else {
			System.out.println("x가 y보다 큽니다.");
		}

		System.out.println("END");
	}

}
